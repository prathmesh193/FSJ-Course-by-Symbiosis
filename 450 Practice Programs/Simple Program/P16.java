//16.	How to Variable Declaration and Initialization in Java

public class P16 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("a = " + a + " b = " + b);
        
        char c = 'c';
        char d = 'd';
        System.out.println("c = " + c + " d = " + d);

        String e = "Hello";
        String f = "World";
        System.out.println("e = " + e + " f = " + f);

        Boolean g= true;
        boolean h= false;
        System.out.println("g = " + g + " h = " + h);
    }
}
